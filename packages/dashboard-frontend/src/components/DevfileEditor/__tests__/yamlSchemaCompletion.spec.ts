const autocompletionMock = autocompletion as jest.MockedFunction<typeof autocompletion>;

/**
 * Gets the last autocompletion config passed to the mock.
 */
function getLastAutocompletionConfig(): AutocompletionConfig {
  const lastCall = autocompletionMock.mock.calls[autocompletionMock.mock.calls.length - 1];
  return lastCall[0] as AutocompletionConfig;
}

/**
 * Gets the completion source function from the last autocompletion call.
 */
function getLastCompletionSource(): CompletionSource | undefined {
  const config = getLastAutocompletionConfig();
  return config.override?.[0];
}

/**
 * Creates a mock CompletionContext for a given document text and cursor position.
 */
